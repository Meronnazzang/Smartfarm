/* USER CODE BEGIN 0 */
void HAL_TIM_PeriodElapsedCallback(TIM_HandleTypeDef *htim)
{
	timerCount++;
	if (timerCount >= 1000)
	{
		timerCount = 1;

		if (HAL_ADC_Start(&hadc1) != HAL_OK) { Error_Handler();	}
		HAL_ADC_PollForConversion(&hadc1, 10);

		uint16_t adc = HAL_ADC_GetValue(&hadc1);
		uint64_t voltage = adc * 330 / 4095;
		uint16_t temperature = voltage;

		uint16_t TxSize = sprintf((char*)TxBuffer, "T:%04u,%02u.%02u,%02u\r\n", adc, (uint16_t)(voltage / 100), (uint16_t)(voltage % 100), temperature);
		HAL_UART_Transmit(&huart3, (uint8_t*)TxBuffer, TxSize, 5000);
	}
}
